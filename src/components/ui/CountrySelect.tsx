// Build fast lookup maps once (module scope)
const NAME_TO_CODE = new Map<string, string>(
  COUNTRIES.map((c) => [c.name.trim().toLowerCase(), c.code])
);
const CODE_SET = new Set(COUNTRIES.map((c) => c.code));

export function normalizeCountryToISO2(
  input: string | null | undefined
): string {
  const raw = String(input ?? "").trim();
  if (!raw) return "";

  // already ISO-2?
  if (/^[A-Za-z]{2}$/.test(raw)) {
    const code = raw.toUpperCase();
    return CODE_SET.has(code) ? code : "";
  }

  // name -> code (case-insensitive)
  const byName = NAME_TO_CODE.get(raw.toLowerCase());
  return byName ?? "";
}

